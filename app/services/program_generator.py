def is_program_text(text: str) -> bool:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—Å—Ç –ø—Ä–æ–≥—Ä–∞–º–º–æ–π —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ–∫.
    
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ:
    - –î–Ω–µ–π (–î–µ–Ω—å 1, –î–µ–Ω—å 2, –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫, –ü—è—Ç–Ω–∏—Ü–∞, –î–µ–Ω—å –Ω–æ–≥ –∏ —Ç.–¥.)
    - –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–π —Å –ø–æ–¥—Ö–æ–¥–∞–º–∏ (—Ñ–æ—Ä–º–∞—Ç "–ù–∞–∑–≤–∞–Ω–∏–µ ‚Äî 12-10-8" –∏–ª–∏ "–ù–∞–∑–≤–∞–Ω–∏–µ ‚Äî 4—Ö10")
    """
    if not text or len(text.strip()) < 20:
        return False
    
    text_lower = text.lower()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –¥–Ω–µ–π (—Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –ø–∞—Ç—Ç–µ—Ä–Ω–æ–≤)
    has_days = bool(
        re.search(r'–¥–µ–Ω—å\s+\d+', text_lower) or  # –î–µ–Ω—å 1, –î–µ–Ω—å 2
        re.search(r'–¥–µ–Ω—å\s+(–Ω–æ–≥|—Å–ø–∏–Ω—ã|–≥—Ä—É–¥–∏|—Ä—É–∫|–ø–ª–µ—á)', text_lower) or  # –î–µ–Ω—å –Ω–æ–≥, –î–µ–Ω—å —Å–ø–∏–Ω—ã
        re.search(r'\b(–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫|–≤—Ç–æ—Ä–Ω–∏–∫|—Å—Ä–µ–¥–∞|—á–µ—Ç–≤–µ—Ä–≥|–ø—è—Ç–Ω–∏—Ü–∞|—Å—É–±–±–æ—Ç–∞|–≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ|–ø–Ω|–≤—Ç|—Å—Ä|—á—Ç|–ø—Ç|—Å–±|–≤—Å)\b', text_lower)  # –î–Ω–∏ –Ω–µ–¥–µ–ª–∏
    )
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π —Å –ø–æ–¥—Ö–æ–¥–∞–º–∏
    # –ü–∞—Ç—Ç–µ—Ä–Ω—ã: "‚Äî 12-10-8", "‚Äî 4—Ö10", "- 4x10", "‚Äî 4 –ø–æ–¥—Ö–æ–¥–∞"
    has_exercises = bool(
        re.search(r'[‚Äî‚Äì-]\s*\d+[x—Ö\-]', text) or
        re.search(r'[‚Äî‚Äì-]\s*\d+\s+–ø–æ–¥—Ö–æ–¥', text_lower) or
        re.search(r'[‚Äî‚Äì-]\s*\d+-\d+', text)
    )
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å –∏ –¥–Ω–∏, –∏ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è - —ç—Ç–æ –ø—Ä–æ–≥—Ä–∞–º–º–∞
    if has_days and has_exercises:
        return True
    
    # –ï—Å–ª–∏ –Ω–µ—Ç —è–≤–Ω—ã—Ö –¥–Ω–µ–π, –Ω–æ –µ—Å—Ç—å –º–Ω–æ–≥–æ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π (3+), —Ç–æ–∂–µ —Å—á–∏—Ç–∞–µ–º –ø—Ä–æ–≥—Ä–∞–º–º–æ–π
    if not has_days:
        exercise_lines = []
        for line in text.split('\n'):
            line = line.strip()
            if ("‚Äî" in line or "-" in line or "—Ö" in line.lower() or "x" in line.lower()):
                if re.search(r'\d+[x—Ö\-]', line) or re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE):
                    exercise_lines.append(line)
        
        if len(exercise_lines) >= 3:
            return True
    
    return False


