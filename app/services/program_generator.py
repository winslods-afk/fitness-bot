def is_program_text(text: str) -> bool:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Ç–µ–∫—Å—Ç –ø—Ä–æ–≥—Ä–∞–º–º–æ–π —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ–∫.
    
    –ü—Ä–æ–≤–µ—Ä—è–µ—Ç –Ω–∞–ª–∏—á–∏–µ:
    - –î–Ω–µ–π (–î–µ–Ω—å 1, –î–µ–Ω—å 2, –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫, –ü—è—Ç–Ω–∏—Ü–∞, –î–µ–Ω—å –Ω–æ–≥, –ü–ù, –í–¢ –∏ —Ç.–¥.)
    - –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–π —Å –ø–æ–¥—Ö–æ–¥–∞–º–∏ (—Ñ–æ—Ä–º–∞—Ç "–ù–∞–∑–≤–∞–Ω–∏–µ ‚Äî 12-10-8", "–ù–∞–∑–≤–∞–Ω–∏–µ ‚Äî 4—Ö10", "3 –ø–æ–¥—Ö–æ–¥–∞", "3*10-12")
    """
    if not text or len(text.strip()) < 20:
        return False
    
    text_lower = text.lower()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ –¥–Ω–µ–π (—Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫ –ø–∞—Ç—Ç–µ—Ä–Ω–æ–≤)
    has_days = bool(
        re.search(r'–¥–µ–Ω—å\s+\d+', text_lower) or  # –î–µ–Ω—å 1, –î–µ–Ω—å 2
        re.search(r'–¥–µ–Ω—å\s+(–Ω–æ–≥|—Å–ø–∏–Ω—ã|–≥—Ä—É–¥–∏|—Ä—É–∫|–ø–ª–µ—á)', text_lower) or  # –î–µ–Ω—å –Ω–æ–≥, –î–µ–Ω—å —Å–ø–∏–Ω—ã
        re.search(r'\b(–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫|–≤—Ç–æ—Ä–Ω–∏–∫|—Å—Ä–µ–¥–∞|—á–µ—Ç–≤–µ—Ä–≥|–ø—è—Ç–Ω–∏—Ü–∞|—Å—É–±–±–æ—Ç–∞|–≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ|–ø–Ω|–≤—Ç|—Å—Ä|—á—Ç|–ø—Ç|—Å–±|–≤—Å)\b', text_lower)  # –î–Ω–∏ –Ω–µ–¥–µ–ª–∏
    )
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞–ª–∏—á–∏–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π —Å –ø–æ–¥—Ö–æ–¥–∞–º–∏
    # –ü–∞—Ç—Ç–µ—Ä–Ω—ã: "‚Äî 12-10-8", "‚Äî 4—Ö10", "- 4x10", "‚Äî 4√ó8‚Äì10", "‚Äî 4 –ø–æ–¥—Ö–æ–¥–∞", "3 –ø–æ–¥—Ö–æ–¥–∞ 12 –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π", "3*10-12", "-—É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ 3 –ø–æ–¥—Ö–æ–¥–∞"
    has_exercises = bool(
        re.search(r'[‚Äî‚Äì-]\s*\d+[x—Ö√ó*\-‚Äì‚Äî]', text) or  # "‚Äî 12-10-8", "‚Äî 4—Ö10"
        re.search(r'[‚Äî‚Äì-]\s*\d+\s+–ø–æ–¥—Ö–æ–¥', text_lower) or  # "‚Äî 4 –ø–æ–¥—Ö–æ–¥–∞"
        re.search(r'[‚Äî‚Äì-]\s*\d+[‚Äì‚Äî]\d+', text) or  # –î–∏–∞–ø–∞–∑–æ–Ω—ã —Å –¥–ª–∏–Ω–Ω—ã–º —Ç–∏—Ä–µ
        re.search(r'[‚Äî‚Äì-]\s*\d+[x—Ö√ó*]\d+[‚Äì‚Äî]\d+', text) or  # –§–æ—Ä–º–∞—Ç "4√ó8‚Äì10"
        re.search(r'^\s*[-‚Äì‚Äî]\s*.*\d+\s+–ø–æ–¥—Ö–æ–¥', text, re.MULTILINE | re.IGNORECASE) or  # –°—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –¥–µ—Ñ–∏—Å–∞ –∏ —Å–æ–¥–µ—Ä–∂–∏—Ç "–ø–æ–¥—Ö–æ–¥"
        re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥.*\d+\s+–ø–æ–≤—Ç–æ—Ä', text_lower) or  # "3 –ø–æ–¥—Ö–æ–¥–∞ 12 –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π"
        re.search(r'\d+[*x—Ö√ó]\d+[-‚Äì‚Äî]?\d*', text) or  # "3*10-12", "3x10", "2—Ö10"
        re.search(r'^\s*[-‚Äì‚Äî]\s*.*\d+[*x—Ö√ó]\d+', text, re.MULTILINE)  # –°—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –¥–µ—Ñ–∏—Å–∞ –∏ —Å–æ–¥–µ—Ä–∂–∏—Ç "3*10"
    )
    
    # –ï—Å–ª–∏ –µ—Å—Ç—å –∏ –¥–Ω–∏, –∏ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è - —ç—Ç–æ –ø—Ä–æ–≥—Ä–∞–º–º–∞
    if has_days and has_exercises:
        return True
    
    # –ï—Å–ª–∏ –Ω–µ—Ç —è–≤–Ω—ã—Ö –¥–Ω–µ–π, –Ω–æ –µ—Å—Ç—å –º–Ω–æ–≥–æ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π (3+), —Ç–æ–∂–µ —Å—á–∏—Ç–∞–µ–º –ø—Ä–æ–≥—Ä–∞–º–º–æ–π
    if not has_days:
        exercise_lines = []
        for line in text.split('\n'):
            line = line.strip()
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º —Ä–∞–∑–ª–∏—á–Ω—ã–µ —Ñ–æ—Ä–º–∞—Ç—ã —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π
            if (("‚Äî" in line or "-" in line or "‚Äì" in line) or 
                "—Ö" in line.lower() or "x" in line.lower() or "*" in line or "√ó" in line):
                if (re.search(r'\d+[x—Ö√ó*\-‚Äì‚Äî]', line) or 
                    re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE) or
                    re.search(r'\d+[*x—Ö√ó]\d+[-‚Äì‚Äî]?\d*', line)):
                    exercise_lines.append(line)
        
        if len(exercise_lines) >= 3:
            return True
    
    return False


