def _is_day_header(line: str) -> Optional[str]:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –¥–Ω—è —Ç—Ä–µ–Ω–∏—Ä–æ–≤–∫–∏.
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è –∏–ª–∏ None.
    
    –†–∞—Å–ø–æ–∑–Ω–∞—ë—Ç:
    - –î–µ–Ω—å 1, –î–µ–Ω—å 2, –î–ï–ù–¨ 1
    - –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫, –í—Ç–æ—Ä–Ω–∏–∫, –°—Ä–µ–¥–∞, –ß–µ—Ç–≤–µ—Ä–≥, –ü—è—Ç–Ω–∏—Ü–∞, –°—É–±–±–æ—Ç–∞, –í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ
    - –ü–ù, –í–¢, –°–†, –ß–¢, –ü–¢, –°–ë, –í–° (–∑–∞–≥–ª–∞–≤–Ω—ã–º–∏ –±—É–∫–≤–∞–º–∏)
    - –î–µ–Ω—å –Ω–æ–≥, –î–µ–Ω—å —Å–ø–∏–Ω—ã, –î–µ–Ω—å –≥—Ä—É–¥–∏ –∏ —Ç.–¥.
    - –ü—è—Ç–Ω–∏—Ü–∞/–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ –∏ —Ç.–¥. (–±–µ–∑ —Å–ª–æ–≤–∞ "–¥–µ–Ω—å")
    - üîπ –ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ ‚Äî –°–ø–∏–Ω–∞ / –ë–∏—Ü–µ–ø—Å (—Å —ç–º–æ–¥–∑–∏ –∏ –¥–ª–∏–Ω–Ω—ã–º —Ç–∏—Ä–µ)
    """
    # –£–±–∏—Ä–∞–µ–º —ç–º–æ–¥–∑–∏ –∏ —Å–ø–µ—Ü–∏–∞–ª—å–Ω—ã–µ —Å–∏–º–≤–æ–ª—ã –≤ –Ω–∞—á–∞–ª–µ —Å—Ç—Ä–æ–∫–∏
    line_clean = re.sub(r'^[üîπüî∏‚ñ™Ô∏è‚ñ´Ô∏è‚Ä¢\*\-\s]+', '', line.strip())
    if not line_clean:
        return None
    
    # –î–Ω–∏ –Ω–µ–¥–µ–ª–∏ (–ø–æ–ª–Ω—ã–µ –Ω–∞–∑–≤–∞–Ω–∏—è –∏ —Å–æ–∫—Ä–∞—â–µ–Ω–∏—è)
    weekdays = {
        '–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫': '–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫',
        '–≤—Ç–æ—Ä–Ω–∏–∫': '–í—Ç–æ—Ä–Ω–∏–∫',
        '—Å—Ä–µ–¥–∞': '–°—Ä–µ–¥–∞',
        '—á–µ—Ç–≤–µ—Ä–≥': '–ß–µ—Ç–≤–µ—Ä–≥',
        '–ø—è—Ç–Ω–∏—Ü–∞': '–ü—è—Ç–Ω–∏—Ü–∞',
        '—Å—É–±–±–æ—Ç–∞': '–°—É–±–±–æ—Ç–∞',
        '–≤–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ': '–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ',
        '–ø–Ω': '–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫',
        '–≤—Ç': '–í—Ç–æ—Ä–Ω–∏–∫',
        '—Å—Ä': '–°—Ä–µ–¥–∞',
        '—á—Ç': '–ß–µ—Ç–≤–µ—Ä–≥',
        '–ø—Ç': '–ü—è—Ç–Ω–∏—Ü–∞',
        '—Å–±': '–°—É–±–±–æ—Ç–∞',
        '–≤—Å': '–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ'
    }
    
    line_lower = line_clean.lower()
    
    # –ü—Ä–æ–≤–µ—Ä—è–µ–º –¥–Ω–∏ –Ω–µ–¥–µ–ª–∏
    for key, value in weekdays.items():
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º –Ω–∞—á–∞–ª–æ —Å—Ç—Ä–æ–∫–∏ (–ø–æ—Å–ª–µ –æ—á–∏—Å—Ç–∫–∏ –æ—Ç —ç–º–æ–¥–∑–∏)
        # –¢–∞–∫–∂–µ –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ –¥–ª—è —Å–æ–∫—Ä–∞—â–µ–Ω–∏–π (–ü–ù, –í–¢ –∏ —Ç.–¥.)
        if line_lower == key or line_lower.startswith(key):
            # –ò–∑–≤–ª–µ–∫–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–æ—Å–ª–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
            # –§–æ—Ä–º–∞—Ç—ã: "–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ ‚Äî –°–ø–∏–Ω–∞", "–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫: –°–ø–∏–Ω–∞", "–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫ / –°–ø–∏–Ω–∞"
            # –î–ª—è —Ç–æ—á–Ω–æ–≥–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è (—Ç–æ–ª—å–∫–æ "–ü–ù", "–í–¢" –∏ —Ç.–¥.) –Ω–µ –∏–∑–≤–ª–µ–∫–∞–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ
            if line_lower == key:
                # –¢–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ - –≤–æ–∑–≤—Ä–∞—â–∞–µ–º —Ç–æ–ª—å–∫–æ –¥–µ–Ω—å
                return value
            
            # –ò—Å–ø–æ–ª—å–∑—É–µ–º –∏—Å—Ö–æ–¥–Ω—É—é —Å—Ç—Ä–æ–∫—É –¥–ª—è –∏–∑–≤–ª–µ—á–µ–Ω–∏—è –Ω–∞–∑–≤–∞–Ω–∏—è
            remaining = line_clean[len(key):].strip()
            
            # –£–±–∏—Ä–∞–µ–º —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–∏ (‚Äî, :, /, -)
            remaining = re.sub(r'^[‚Äî‚Äì:\-/\s]+', '', remaining)
            
            if remaining:
                # –ï—Å–ª–∏ –µ—Å—Ç—å –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–æ—Å–ª–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
                return f"{value}: {remaining}"
            return value
    
    # –î–µ–Ω—å —Å –Ω–æ–º–µ—Ä–æ–º: "–î–µ–Ω—å 1", "–î–µ–Ω—å 2", "–î–ï–ù–¨ 1: –ù–∞–∑–≤–∞–Ω–∏–µ", "–î–µ–Ω—å 1 ‚Äî –ì—Ä—É–¥—å –∏ –¢—Ä–∏—Ü–µ–ø—Å"
    # –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º –¥–ª–∏–Ω–Ω–æ–µ —Ç–∏—Ä–µ (‚Äî) –∏ –æ–±—ã—á–Ω–æ–µ (-)
    day_num_match = re.match(r'–¥–µ–Ω—å\s+(\d+)[:\-‚Äî‚Äì/\s]+(.+)', line_clean, re.IGNORECASE)
    if day_num_match:
        day_name = day_num_match.group(2).strip()
        if day_name:
            return day_name
        return f"–î–µ–Ω—å {day_num_match.group(1)}"
    
    # –¢–∞–∫–∂–µ –ø—Ä–æ–≤–µ—Ä—è–µ–º —Ñ–æ—Ä–º–∞—Ç –±–µ–∑ –ø—Ä–æ–±–µ–ª–∞: "–î–µ–Ω—å1", "–î–µ–Ω—å2"
    day_num_match_no_space = re.match(r'–¥–µ–Ω—å\s*(\d+)[:\-‚Äî‚Äì/\s]+(.+)', line_clean, re.IGNORECASE)
    if day_num_match_no_space:
        day_name = day_num_match_no_space.group(2).strip()
        if day_name:
            return day_name
        return f"–î–µ–Ω—å {day_num_match_no_space.group(1)}"
    
    # –î–µ–Ω—å —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º –≥—Ä—É–ø–ø—ã –º—ã—à—Ü: "–î–µ–Ω—å –Ω–æ–≥", "–î–µ–Ω—å —Å–ø–∏–Ω—ã", "–î–µ–Ω—å –≥—Ä—É–¥–∏"
    day_muscle_match = re.match(r'–¥–µ–Ω—å\s+(–Ω–æ–≥|—Å–ø–∏–Ω—ã|–≥—Ä—É–¥–∏|—Ä—É–∫|–ø–ª–µ—á|–±–∏—Ü–µ–ø—Å–∞|—Ç—Ä–∏—Ü–µ–ø—Å–∞|–ø—Ä–µ—Å—Å–∞|–∫–∞—Ä–¥–∏–æ)', line_clean, re.IGNORECASE)
    if day_muscle_match:
        muscle = day_muscle_match.group(1)
        # –ò–∑–≤–ª–µ–∫–∞–µ–º –ø–æ–ª–Ω–æ–µ –Ω–∞–∑–≤–∞–Ω–∏–µ, –µ—Å–ª–∏ –µ—Å—Ç—å –ø–æ—Å–ª–µ —Ä–∞–∑–¥–µ–ª–∏—Ç–µ–ª–µ–π
        remaining = line_clean[day_muscle_match.end():].strip()
        remaining = re.sub(r'^[‚Äî‚Äì:\-/\s]+', '', remaining)
        if remaining:
            return f"–î–µ–Ω—å {muscle}: {remaining}"
        return f"–î–µ–Ω—å {muscle}"
    
    # –ü—Ä–æ—Å—Ç–æ –Ω–∞–∑–≤–∞–Ω–∏–µ –≥—Ä—É–ø–ø—ã –º—ã—à—Ü –≤ –Ω–∞—á–∞–ª–µ —Å—Ç—Ä–æ–∫–∏: "–ù–æ–≥–∏", "–°–ø–∏–Ω–∞", "–ì—Ä—É–¥—å"
    muscle_groups = ['–Ω–æ–≥–∏', '—Å–ø–∏–Ω–∞', '–≥—Ä—É–¥—å', '—Ä—É–∫–∏', '–ø–ª–µ—á–∏', '–±–∏—Ü–µ–ø—Å', '—Ç—Ä–∏—Ü–µ–ø—Å', '–ø—Ä–µ—Å—Å', '–∫–∞—Ä–¥–∏–æ']
    for muscle in muscle_groups:
        if line_lower.startswith(muscle) and len(line_clean) < 80:  # –ö–æ—Ä–æ—Ç–∫–∞—è —Å—Ç—Ä–æ–∫–∞, –≤–µ—Ä–æ—è—Ç–Ω–æ –∑–∞–≥–æ–ª–æ–≤–æ–∫
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —ç—Ç–æ –Ω–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ (—É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è –æ–±—ã—á–Ω–æ —Å–æ–¥–µ—Ä–∂–∞—Ç –ø–æ–¥—Ö–æ–¥—ã —Å —á–∏—Å–ª–∞–º–∏)
            if not re.search(r'[‚Äî‚Äì-]\s*\d+[x—Ö\-]', line_clean) and not re.search(r'[‚Äî‚Äì-]\s*\d+-\d+', line_clean):
                remaining = line_clean[len(muscle):].strip()
                remaining = re.sub(r'^[‚Äî‚Äì:\-/\s]+', '', remaining)
                if remaining:
                    return f"{muscle.capitalize()}: {remaining}"
                return muscle.capitalize()
    
    return None


def parse_ai_program_response(ai_response: str) -> Optional[Dict]:
    """
    –ü–∞—Ä—Å–∏—Ç –æ—Ç–≤–µ—Ç AI —Å –ø—Ä–æ–≥—Ä–∞–º–º–æ–π —Ç—Ä–µ–Ω–∏—Ä–æ–≤–æ–∫.
    
    –û–∂–∏–¥–∞–µ–º—ã–π —Ñ–æ—Ä–º–∞—Ç –æ—Ç AI:
    –ü–†–û–ì–†–ê–ú–ú–ê: –ù–∞–∑–≤–∞–Ω–∏–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã
    
    –î–ï–ù–¨ 1: –ù–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è
    –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ 1 ‚Äî –ø–æ–¥—Ö–æ–¥—ã
    –£–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ 2 ‚Äî –ø–æ–¥—Ö–æ–¥—ã
    ...
    
    –î–ï–ù–¨ 2: –ù–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è
    ...
    
    –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ª–æ–≤–∞—Ä—å —Å –ø—Ä–æ–≥—Ä–∞–º–º–æ–π –∏–ª–∏ None, –µ—Å–ª–∏ –Ω–µ —É–¥–∞–ª–æ—Å—å —Ä–∞—Å–ø–∞—Ä—Å–∏—Ç—å.
    """
    lines = ai_response.strip().split('\n')
    
    program_name = None
    days = []
    current_day = None
    
    for line in lines:
        line = line.strip()
        if not line:
            continue
        
        # –ü–∞—Ä—Å–∏–º –Ω–∞–∑–≤–∞–Ω–∏–µ –ø—Ä–æ–≥—Ä–∞–º–º—ã
        if line.upper().startswith("–ü–†–û–ì–†–ê–ú–ú–ê:"):
            program_name = line.split(":", 1)[1].strip()
            continue
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –¥–Ω—è
        day_name = _is_day_header(line)
        if day_name:
            # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –¥–µ–Ω—å, –µ—Å–ª–∏ –µ—Å—Ç—å
            if current_day:
                days.append(current_day)
            
            current_day = {
                "name": day_name,
                "exercises": []
            }
            continue
        
        # –ü–∞—Ä—Å–∏–º —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ (–µ—Å–ª–∏ –µ—Å—Ç—å —Ç–µ–∫—É—â–∏–π –¥–µ–Ω—å)
        if current_day and ("‚Äî" in line or "-" in line or "—Ö" in line.lower() or "x" in line.lower()):
            # –£–±–∏—Ä–∞–µ–º –Ω—É–º–µ—Ä–∞—Ü–∏—é, –µ—Å–ª–∏ –µ—Å—Ç—å (–Ω–∞–ø—Ä–∏–º–µ—Ä, "1. " –∏–ª–∏ "‚Ä¢ ")
            exercise_line = re.sub(r'^[\d‚Ä¢\-\*]\s*', '', line).strip()
            
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —ç—Ç–æ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ (—Å–æ–¥–µ—Ä–∂–∏—Ç –ø–∞—Ç—Ç–µ—Ä–Ω –ø–æ–¥—Ö–æ–¥–æ–≤)
            if re.search(r'\d+[x—Ö\-]', exercise_line) or re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', exercise_line, re.IGNORECASE):
                current_day["exercises"].append(exercise_line)
    
    # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π –¥–µ–Ω—å
    if current_day:
        days.append(current_day)
    
    # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ –ø—Ä–æ–≥—Ä–∞–º–º—É —á–µ—Ä–µ–∑ —Ñ–æ—Ä–º–∞—Ç, –ø—ã—Ç–∞–µ–º—Å—è –Ω–∞–π—Ç–∏ –≤ —Å–≤–æ–±–æ–¥–Ω–æ–º —Ç–µ–∫—Å—Ç–µ
    if not program_name or not days:
        return _parse_free_format(ai_response)
    
    if program_name and days:
        return {
            "name": program_name,
            "days": days
        }
    
    return None


def _is_descriptive_line(line: str) -> bool:
    """
    –û–ø—Ä–µ–¥–µ–ª—è–µ—Ç, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –æ–ø–∏—Å–∞—Ç–µ–ª—å–Ω–æ–π (–Ω–µ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ–º –∏ –Ω–µ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –¥–Ω—è).
    –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫–∏ —Ç–∏–ø–∞:
    - "–¶–µ–ª—å ‚Äî —Ç—è–≥–æ–≤–æ–π –∞–∫—Ü–µ–Ω—Ç"
    - "–û—Ç–¥—ã—Ö 90‚Äì120 —Å–µ–∫"
    - "–†–∞–∑–º–∏–Ω–∫–∞: 5‚Äì10 –º–∏–Ω"
    - "–û—Å–Ω–æ–≤–Ω–∞—è —á–∞—Å—Ç—å:"
    - "(–º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å...)"
    """
    line_lower = line.lower().strip()
    
    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏
    if not line_lower:
        return True
    
    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º —Å—Ç—Ä–æ–∫–∏ –≤ —Å–∫–æ–±–∫–∞—Ö (–∫–æ–º–º–µ–Ω—Ç–∞—Ä–∏–∏)
    if line_lower.startswith('(') and line_lower.endswith(')'):
        return True
    
    # –ò–≥–Ω–æ—Ä–∏—Ä—É–µ–º –æ–ø–∏—Å–∞—Ç–µ–ª—å–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏
    descriptive_patterns = [
        r'^—Ü–µ–ª—å\s*[‚Äî‚Äì:\-]',  # "–¶–µ–ª—å ‚Äî ..."
        r'^–æ—Ç–¥—ã—Ö\s*\d+',  # "–û—Ç–¥—ã—Ö 90‚Äì120"
        r'^—Ä–∞–∑–º–∏–Ω–∫–∞\s*[:]',  # "–†–∞–∑–º–∏–Ω–∫–∞:"
        r'^–æ—Å–Ω–æ–≤–Ω–∞—è\s+—á–∞—Å—Ç—å',  # "–û—Å–Ω–æ–≤–Ω–∞—è —á–∞—Å—Ç—å:"
        r'^–≤–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ–ª—å–Ω—ã–π',  # "–í–æ—Å—Å—Ç–∞–Ω–æ–≤–∏—Ç–µ–ª—å–Ω—ã–π..."
        r'^–º–æ–∂–Ω–æ\s+–¥–æ–±–∞–≤–∏—Ç—å',  # "–º–æ–∂–Ω–æ –¥–æ–±–∞–≤–∏—Ç—å..."
        r'^–Ω–∞–ø—Ä–∏–º–µ—Ä',  # "–Ω–∞–ø—Ä–∏–º–µ—Ä..."
        r'^\d+\s*[–º–º–∏–Ω—Å–µ–∫]',  # "5‚Äì10 –º–∏–Ω", "90‚Äì120 —Å–µ–∫"
    ]
    
    for pattern in descriptive_patterns:
        if re.match(pattern, line_lower, re.IGNORECASE):
            return True
    
    # –ï—Å–ª–∏ —Å—Ç—Ä–æ–∫–∞ –æ—á–µ–Ω—å –∫–æ—Ä–æ—Ç–∫–∞—è –∏ –Ω–µ —Å–æ–¥–µ—Ä–∂–∏—Ç —á–∏—Å–µ–ª —Å –ø–æ–¥—Ö–æ–¥–∞–º–∏ - –≤–µ—Ä–æ—è—Ç–Ω–æ –æ–ø–∏—Å–∞–Ω–∏–µ
    # –ù–æ –Ω–µ –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º, –µ—Å–ª–∏ —ç—Ç–æ –º–æ–∂–µ—Ç –±—ã—Ç—å –∑–∞–≥–æ–ª–æ–≤–æ–∫ –¥–Ω—è (–ø—Ä–æ–≤–µ—Ä—è–µ–º —á–µ—Ä–µ–∑ –ø–∞—Ç—Ç–µ—Ä–Ω—ã)
    if len(line_lower) < 15 and not re.search(r'\d+[x—Ö\-]', line) and not re.search(r'\d+-\d+', line):
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–µ —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —ç—Ç–æ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –¥–Ω—è (–±–µ–∑ –≤—ã–∑–æ–≤–∞ —Ñ—É–Ω–∫—Ü–∏–∏, —á—Ç–æ–±—ã –∏–∑–±–µ–∂–∞—Ç—å —Ä–µ–∫—É—Ä—Å–∏–∏)
        if not (re.match(r'–¥–µ–Ω—å\s+\d+', line_lower) or 
                re.match(r'–¥–µ–Ω—å\s+(–Ω–æ–≥|—Å–ø–∏–Ω—ã|–≥—Ä—É–¥–∏)', line_lower) or
                re.match(r'(–ø–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫|–≤—Ç–æ—Ä–Ω–∏–∫|—Å—Ä–µ–¥–∞|—á–µ—Ç–≤–µ—Ä–≥|–ø—è—Ç–Ω–∏—Ü–∞)', line_lower)):
            return True
    
    return False


def _parse_free_format(text: str) -> Optional[Dict]:
    """
    –ü–∞—Ä—Å–∏—Ç –ø—Ä–æ–≥—Ä–∞–º–º—É –∏–∑ —Å–≤–æ–±–æ–¥–Ω–æ–≥–æ —Ñ–æ—Ä–º–∞—Ç–∞.
    –£–ª—É—á—à–µ–Ω–Ω–∞—è –≤–µ—Ä—Å–∏—è, –∫–æ—Ç–æ—Ä–∞—è —Ä–∞—Å–ø–æ–∑–Ω–∞—ë—Ç —Ä–∞–∑–ª–∏—á–Ω—ã–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã –¥–Ω–µ–π.
    
    –û–±—Ä–∞–±–∞—Ç—ã–≤–∞–µ—Ç —Å–ª—É—á–∞–∏:
    - –ü–ù\n–ì—Ä—É–¥—å\n-—É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ
    - –ü–ù ‚Äî –ì—Ä—É–¥—å\n-—É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ
    - –î–µ–Ω—å 1: –ù–∞–∑–≤–∞–Ω–∏–µ\n-—É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ
    - –ü–æ–¥–∑–∞–≥–æ–ª–æ–≤–∫–∏ –≥—Ä—É–ø–ø –º—ã—à—Ü (–†—É–∫–∏, –ü–ª–µ—á–∏) –≤–Ω—É—Ç—Ä–∏ –¥–Ω—è
    """
    # –ò—â–µ–º –ø–∞—Ç—Ç–µ—Ä–Ω—ã —Ç–∏–ø–∞ "–ü—Ä–æ–≥—Ä–∞–º–º–∞: ..." –∏–ª–∏ "–ù–∞–∑–≤–∞–Ω–∏–µ: ..."
    program_match = re.search(r'(?:–ü—Ä–æ–≥—Ä–∞–º–º–∞|–ù–∞–∑–≤–∞–Ω–∏–µ)[:\-]\s*(.+)', text, re.IGNORECASE)
    program_name = program_match.group(1).strip() if program_match else "–ü—Ä–æ–≥—Ä–∞–º–º–∞ –æ—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è"
    
    lines = text.strip().split('\n')
    days = []
    current_day = None
    pending_day_name = None  # –ù–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è, –∫–æ—Ç–æ—Ä–æ–µ –Ω—É–∂–Ω–æ –ø—Ä–∏–º–µ–Ω–∏—Ç—å –∫ —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–µ
    muscle_group_subheaders = ['—Ä—É–∫–∏', '–Ω–æ–≥–∏', '–ø–ª–µ—á–∏', '–≥—Ä—É–¥—å', '—Å–ø–∏–Ω–∞', '–±–∏—Ü–µ–ø—Å', '—Ç—Ä–∏—Ü–µ–ø—Å', '–ø—Ä–µ—Å—Å', '–∫–∞—Ä–¥–∏–æ']
    
    for i, line in enumerate(lines):
        line = line.strip()
        if not line:
            continue
        
        # –ü—Ä–æ–ø—É—Å–∫–∞–µ–º –æ–ø–∏—Å–∞—Ç–µ–ª—å–Ω—ã–µ —Å—Ç—Ä–æ–∫–∏
        if _is_descriptive_line(line):
            continue
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –∑–∞–≥–æ–ª–æ–≤–∫–æ–º –¥–Ω—è
        day_name = _is_day_header(line)
        if day_name:
            # –°–æ—Ö—Ä–∞–Ω—è–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–π –¥–µ–Ω—å, –µ—Å–ª–∏ –µ—Å—Ç—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è
            if current_day and current_day["exercises"]:
                days.append(current_day)
            elif current_day and not current_day["exercises"]:
                # –î–µ–Ω—å –±–µ–∑ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–π - –ø—Ä–æ–ø—É—Å–∫–∞–µ–º
                pass
            
            # –ï—Å–ª–∏ –¥–µ–Ω—å –Ω–µ–¥–µ–ª–∏ –±–µ–∑ –Ω–∞–∑–≤–∞–Ω–∏—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, "–ü–ù"), —Å–æ—Ö—Ä–∞–Ω—è–µ–º –¥–ª—è —Å–ª–µ–¥—É—é—â–µ–π —Å—Ç—Ä–æ–∫–∏
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –Ω–∞–∑–≤–∞–Ω–∏–µ –ø–æ—Å–ª–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏
            if ":" not in day_name and day_name in ['–ü–æ–Ω–µ–¥–µ–ª—å–Ω–∏–∫', '–í—Ç–æ—Ä–Ω–∏–∫', '–°—Ä–µ–¥–∞', '–ß–µ—Ç–≤–µ—Ä–≥', '–ü—è—Ç–Ω–∏—Ü–∞', '–°—É–±–±–æ—Ç–∞', '–í–æ—Å–∫—Ä–µ—Å–µ–Ω—å–µ']:
                pending_day_name = day_name
                current_day = {
                    "name": day_name,
                    "exercises": []
                }
            else:
                current_day = {
                    "name": day_name,
                    "exercises": []
                }
                pending_day_name = None
            continue
        
        # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ –Ω–∞–∑–≤–∞–Ω–∏–µ–º –≥—Ä—É–ø–ø—ã –º—ã—à—Ü (–ø–æ—Å–ª–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏)
        line_lower = line.lower()
        is_muscle_group = any(line_lower == mg or line_lower.startswith(mg + ' ') for mg in muscle_group_subheaders)
        
        if pending_day_name and is_muscle_group:
            # –≠—Ç–æ –Ω–∞–∑–≤–∞–Ω–∏–µ –≥—Ä—É–ø–ø—ã –º—ã—à—Ü –ø–æ—Å–ª–µ –¥–Ω—è –Ω–µ–¥–µ–ª–∏ - –æ–±–Ω–æ–≤–ª—è–µ–º –Ω–∞–∑–≤–∞–Ω–∏–µ –¥–Ω—è
            current_day["name"] = f"{pending_day_name}: {line}"
            pending_day_name = None
            continue
        elif is_muscle_group and current_day:
            # –ü–æ–¥–∑–∞–≥–æ–ª–æ–≤–æ–∫ –≥—Ä—É–ø–ø—ã –º—ã—à—Ü –≤–Ω—É—Ç—Ä–∏ –¥–Ω—è (–Ω–∞–ø—Ä–∏–º–µ—Ä, "–†—É–∫–∏" –ø–æ—Å–ª–µ "–ì—Ä—É–¥—å")
            # –ù–µ —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π –¥–µ–Ω—å, –ø—Ä–æ—Å—Ç–æ –ø—Ä–æ–¥–æ–ª–∂–∞–µ–º –¥–æ–±–∞–≤–ª—è—Ç—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è
            continue
        
        # –ï—Å–ª–∏ –µ—Å—Ç—å —Ç–µ–∫—É—â–∏–π –¥–µ–Ω—å, –ø—Ä–æ–≤–µ—Ä—è–µ–º, —è–≤–ª—è–µ—Ç—Å—è –ª–∏ —Å—Ç—Ä–æ–∫–∞ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ–º
        if current_day:
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, —á—Ç–æ —ç—Ç–æ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ (—Å–æ–¥–µ—Ä–∂–∏—Ç –ø–æ–¥—Ö–æ–¥—ã)
            # –ü–æ–¥–¥–µ—Ä–∂–∏–≤–∞–µ–º —Å–∏–º–≤–æ–ª √ó (—É–º–Ω–æ–∂–µ–Ω–∏–µ), * (–∑–≤–µ–∑–¥–æ—á–∫–∞), –¥–ª–∏–Ω–Ω–æ–µ —Ç–∏—Ä–µ, —Ö
            # –§–æ—Ä–º–∞—Ç—ã: "3 –ø–æ–¥—Ö–æ–¥–∞", "3 –ø–æ–¥—Ö–æ–¥–∞ 12 –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π", "3*10-12", "2—Ö10", "3x10-12"
            exercise_patterns = [
                r'\d+\s+–ø–æ–¥—Ö–æ–¥',  # "3 –ø–æ–¥—Ö–æ–¥–∞"
                r'\d+\s+–ø–æ–¥—Ö–æ–¥.*\d+\s+–ø–æ–≤—Ç–æ—Ä',  # "3 –ø–æ–¥—Ö–æ–¥–∞ 12 –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π"
                r'\d+[*x—Ö√ó]\d+',  # "3*10", "3x10", "3—Ö10", "3√ó10"
                r'\d+[*x—Ö√ó]\d+[-‚Äì‚Äî]\d+',  # "3*10-12", "3x10-12"
                r'\d+[-‚Äì‚Äî]\d+',  # "20-12-15" (—Å–ø–∏—Å–æ–∫ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π)
                r'^\s*[-‚Äì‚Äî]\s*',  # –°—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è —Å –¥–µ—Ñ–∏—Å–∞ (–º–∞—Ä–∫–∏—Ä–æ–≤–∞–Ω–Ω—ã–π —Å–ø–∏—Å–æ–∫)
            ]
            
            is_exercise = any(re.search(pattern, line, re.IGNORECASE) for pattern in exercise_patterns)
            
            if is_exercise:
                # –£–±–∏—Ä–∞–µ–º –Ω—É–º–µ—Ä–∞—Ü–∏—é –∏ –º–∞—Ä–∫–µ—Ä—ã, –µ—Å–ª–∏ –µ—Å—Ç—å
                exercise_line = re.sub(r'^[\d‚Ä¢\-\*]\s*', '', line).strip()
                
                # –£–±–∏—Ä–∞–µ–º –Ω–∞—á–∞–ª—å–Ω—ã–π –¥–µ—Ñ–∏—Å, –µ—Å–ª–∏ –µ—Å—Ç—å
                exercise_line = re.sub(r'^\s*[-‚Äì‚Äî]\s+', '', exercise_line).strip()
                
                if exercise_line and len(exercise_line) > 3:
                    current_day["exercises"].append(exercise_line)
        else:
            # –ï—Å–ª–∏ –Ω–µ—Ç —Ç–µ–∫—É—â–µ–≥–æ –¥–Ω—è, –Ω–æ —Å—Ç—Ä–æ–∫–∞ –ø–æ—Ö–æ–∂–∞ –Ω–∞ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏–µ,
            # —Å–æ–∑–¥–∞—ë–º –Ω–æ–≤—ã–π –¥–µ–Ω—å —Å –Ω–∞–∑–≤–∞–Ω–∏–µ–º –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é
            if ("‚Äî" in line or "-" in line or "‚Äì" in line or 
                "—Ö" in line.lower() or "x" in line.lower() or "√ó" in line or "*" in line or
                re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE)):
                if (re.search(r'\d+[x—Ö√ó*\-‚Äì‚Äî]', line) or 
                    re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE) or
                    re.search(r'\d+[x—Ö√ó*]\d+[‚Äì‚Äî]\d+', line)):  # –§–æ—Ä–º–∞—Ç "4√ó8‚Äì10"
                    exercise_line = re.sub(r'^[\d‚Ä¢\-\*]\s*', '', line).strip()
                    exercise_line = re.sub(r'^\s*[-‚Äì‚Äî]\s+', '', exercise_line).strip()
                    if exercise_line and len(exercise_line) > 3:
                        current_day = {
                            "name": f"–î–µ–Ω—å {len(days) + 1}",
                            "exercises": [exercise_line]
                        }
    
    # –î–æ–±–∞–≤–ª—è–µ–º –ø–æ—Å–ª–µ–¥–Ω–∏–π –¥–µ–Ω—å (—Ç–æ–ª—å–∫–æ –µ—Å–ª–∏ –µ—Å—Ç—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è)
    if current_day and current_day["exercises"]:
        days.append(current_day)
    
    # –ï—Å–ª–∏ –Ω–µ –Ω–∞—à–ª–∏ —Å—Ç—Ä—É–∫—Ç—É—Ä–∏—Ä–æ–≤–∞–Ω–Ω—ã–µ –¥–Ω–∏, –ø—ã—Ç–∞–µ–º—Å—è –∏–∑–≤–ª–µ—á—å —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è
    if not days:
        all_exercises = []
        for line in text.split('\n'):
            line = line.strip()
            if ("‚Äî" in line or "-" in line or "‚Äì" in line or 
                "—Ö" in line.lower() or "x" in line.lower() or "√ó" in line or
                re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE)):
                if (re.search(r'\d+[x—Ö√ó\-‚Äì‚Äî]', line) or 
                    re.search(r'\d+\s+–ø–æ–¥—Ö–æ–¥', line, re.IGNORECASE) or
                    re.search(r'\d+[x—Ö√ó]\d+[‚Äì‚Äî]\d+', line)):  # –§–æ—Ä–º–∞—Ç "4√ó8‚Äì10"
                    exercise_line = re.sub(r'^[\d‚Ä¢\-\*]\s*', '', line).strip()
                    if exercise_line and len(exercise_line) > 3:
                        all_exercises.append(exercise_line)
        
        if all_exercises:
            # –°–æ–∑–¥–∞—ë–º –æ–¥–∏–Ω –¥–µ–Ω—å —Å–æ –≤—Å–µ–º–∏ —É–ø—Ä–∞–∂–Ω–µ–Ω–∏—è–º–∏
            days.append({
                "name": "–¢—Ä–µ–Ω–∏—Ä–æ–≤–∫–∞",
                "exercises": all_exercises
            })
    
    if days:
        return {
            "name": program_name,
            "days": days
        }
    
    return None


